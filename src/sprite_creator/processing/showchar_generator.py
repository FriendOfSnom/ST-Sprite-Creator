"""
showChar Ren'Py Statement Generator

Generates two .rpy files per character folder that together provide a
'showChar' custom statement for scenario writers:

    showChar Niya happy at center with dissolve

Instead of the classic:

    show niya a_2 at center with dissolve

Files generated:
  00showchar.rpy      - Shared infrastructure (statement registration, prefix detection)
  showchar_<Name>.rpy - Per-character emotion and outfit mappings
"""

from pathlib import Path
from typing import Dict, List, Optional, Tuple

from ..logging_utils import log_info, log_warning


